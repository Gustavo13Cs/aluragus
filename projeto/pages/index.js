import React from "react";
import { videoService } from "../src/components/services/videoServie";



function HomePage() {
    const estilosDaHomePage = {

        // backgroundColor: "red" 
    };
    const service = videoService();
    const [valordoFiltro, setvalorDofilto] = React.useState("");
    const [playlist, setPlaylists] = React.useState({});
    React.useEffect(() => {
        console.log("useEffect");
        service
            .getAllVideos()
            .then((dados) => {
                console.log(dados.data);
                // Forma imutavel
                const novasPlaylists = {};
                dados.data.forEach((video) => {
                    if (!novasPlaylists[video.playlist]) novasPlaylists[video.playlist] = [];
                    novasPlaylists[video.playlist] = [
                        video,
                        ...novasPlaylists[video.playlist],
                    ];
                });

                setPlaylists(novasPlaylists);
            });
    }, []);
    // console.log(config.playlists);


    return (
        <>
            <div style={{
                display: "flex",
                flexDirection: "column",
                flex: 1,
                // backgroundColor: "red",
            }}>
                <Menu valordoFiltro={valordoFiltro} setvalorDofilto={setvalorDofilto} />
                <Header />
                <Timeline searchValue={valordoFiltro} playlists={config.playlists}>
                    Conteúdo
                </Timeline>
            </div>
        </>
    );
}

export default HomePage

